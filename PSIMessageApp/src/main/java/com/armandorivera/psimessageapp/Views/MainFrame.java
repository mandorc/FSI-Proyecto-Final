    public static void deliverMessage(Client recipient, String fromName, String plaintext, String ctBase64) {
        ClientFrame frame = CLIENT_FRAMES.get(recipient);
        if (frame != null) {
            frame.receiveMessage(fromName, plaintext, ctBase64);
        } else {
            System.err.println("No hay frame para el cliente: " + recipient.getName());
        }
    }

