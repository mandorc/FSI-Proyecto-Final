    private static final java.util.Map<Client, ClientFrame> CLIENT_FRAMES = new java.util.HashMap<>();

    public static CertificateAuthority getCA() {
        return CA;
    }

    public static java.util.List<Client> getClients() {
        return CLIENTS;
    }

    public static void addClient(Client c) {
        CLIENTS.add(c);
    }

    public static void registerClientFrame(Client client, ClientFrame frame) {
        CLIENT_FRAMES.put(client, frame);
    }

    public static ClientFrame getFrameForClient(Client client) {
        return CLIENT_FRAMES.get(client);
    }

    public static Client getClientByName(String name) {
        for (Client c : CLIENTS) {
            if (c.getName().equals(name)) {
                return c;
            }
        }
        return null;
    }

