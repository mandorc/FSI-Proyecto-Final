    private void addClientButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClientButtonActionPerformed
        String fullName = javax.swing.JOptionPane.showInputDialog(this, "Nombre completo del cliente:");
        if (fullName == null) {
            return;
        }
        fullName = fullName.trim();
        if (fullName.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El nombre no puede estar vac칤o.",
                    "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        Object[] options = {"Cl치sico (RSA + AES)", "Post-cu치ntico / H칤brido (Kyber + RSA)"};
        int choice = javax.swing.JOptionPane.showOptionDialog(
                this,
                "Selecciona el tipo de esquema criptogr치fico para este cliente:",
                "Modo criptogr치fico",
                javax.swing.JOptionPane.DEFAULT_OPTION,
                javax.swing.JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[0]
        );

        if (choice == javax.swing.JOptionPane.CLOSED_OPTION) {
            return;
        }

        CryptoMode mode = (choice == 0) ? CryptoMode.CLASSIC : CryptoMode.HYBRID_PQ;

        try {
            // 1) Crear cliente (claves + cert + archivos)
            Client client = MainFrame.getCA().registerNewClient(fullName, mode);
            MainFrame.addClient(client);

            // 2) Crear su frame
            ClientFrame clientFrame = new ClientFrame(client);
            desktopPane.add(clientFrame);
            clientFrame.setClosable(true);
            clientFrame.setIconifiable(true);
            clientFrame.setMaximizable(true);
            clientFrame.setResizable(true);
            clientFrame.setVisible(true);
            clientFrame.setSelected(true);

            // 游댳 3) REGISTRAR el frame para este cliente (esto faltaba)
            MainFrame.registerClientFrame(client, clientFrame);

        } catch (Exception ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al crear el cliente/certificado: " + ex.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addClientButtonActionPerformed

